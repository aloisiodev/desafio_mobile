import SwiftUI

extension Font {
    enum BC {
        static let pageTitle  = Font.system(size: 32, weight: .light)
        static let cardTitle  = Font.system(size: 24, weight: .semibold)
        static let button     = Font.system(size: 16, weight: .medium)
        static let body       = Font.system(size: 14)
        static let caption    = Font.system(size: 13)
    }
}
